public class Rectangle {
  public static void main(String[] args) {
    int length = 5;  
    int width = 2;  
    int area = length * width;  
    int perimeter = 2 * (length + width);
    System.out.println("Area of rectangle: " + area);  
    System.out.println("Perimeter of rectangle: " + perimeter);
  }
}
